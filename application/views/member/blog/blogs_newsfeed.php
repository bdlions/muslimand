                        <p style="text-align: justify;">Rasulullah Sallallahu Alayhi wa Sallam said: "Verily, those who have the most [wealth] will be the ones with least on the Day 
                            of Resurrection except for the one who says, 'This is for that, this for that and this is for that [in other words, he gives the 
                            wealth away for good causes].'" [Bukhari, Muslim] <br><br><br>
                            In general, gathering a lot of wealth in this world takes up a lot of time and effort. Those who gather such wealth spend much 
                            of their time in worldly matters and, hence, they do not have much time to spend for matters of the next world. Unless they use 
                            that wealth in good ways before Allah takes it from them, they will be among the people with the least on the Day of Judgment.<br><br><br>
                            Using wealth in a good way is one of the greatest ways by which one can earn Allah's pleasure. The relevant issue is what the 
                            person does when he has wealth. Does he use it in ways that are beneficial to him for his meeting with his Lord? Or does he use 
                            it in ways that do not benefit him or may even harm him? If he does not use that wealth in a way that is beneficial to himself 
                            in the Hereafter, he has lost a tremendous opportunity. He may never get that opportunity again. When he uses up all of his wealth
                            in frivolous pursuits, there may come a time when he needs all he has simply to support himself and his family.<br><br><br>
                            At that time, the question of him giving for the sake of Allah will no longer be a viable question. He will not have the ability 
                            to give and, worst of all, he demonstrated to Allah that if he had the ability to give, he would not give.<br><br><br>
                            Compiled From: "Commentary on the Forty Hadith of al-Nawawi" </p>
